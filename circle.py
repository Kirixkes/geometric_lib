import math


def area(r):
    '''Функция принимает аргумент r(радиус окружности), и возвращает площадь круга с данным радиусом'''
    return math.pi * r * r


def perimeter(r):
    '''Функция принимает аргумент r(радиус окружности), и возвращает периметр круга с данным радиусом'''
    return 2 * math.pi * r

