def area(a, b, c):
    '''Функция принимает 3 аргумента (стороны треугольника), и возвращает площадь этого треугольника'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Функция принимает 3 аргумента (стороны треугольника), и возвращает периметр этого треугольника'''
    return a + b + c
